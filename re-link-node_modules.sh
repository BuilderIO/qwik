#!/bin/sh -x

declare -a paths=("." "packages/docs" "packages/insights")

for dir in ${paths[@]}; do
  echo $dir
  rm -rf $dir/node_modules/@builder.io/qwik
  ln -s ../../../qwik/dist $dir/node_modules/@builder.io/qwik

  rm -rf $dir/node_modules/@builder.io/qwik-city
  ln -s ../../../qwik-city/lib $dir/node_modules/@builder.io/qwik-city
done

mv ./node_modules/.bin/qwik ./node_modules/.bin/qwik.original
sed 's/qwik\/dist\/qwik/qwik\/qwik/' ./node_modules/.bin/qwik.original > ./node_modules/.bin/qwik
chmod +x ./node_modules/.bin/qwik
